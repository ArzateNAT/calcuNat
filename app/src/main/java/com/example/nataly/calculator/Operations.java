package com.example.nataly.calculator;

import android.support.v7.app.AppCompatActivity;

/**
 * Created by Nataly on 12/03/2017.
 */

public class Operations extends AppCompatActivity {

    public float suma (float value1, float value2 )
    {
       return value1 + value2;
    }

    public float resta (float value1, float value2 )
    {
        return value1 - value2;
    }

    public float multi (float value1, float value2 )
    {
        return value1 * value2;
    }

    public float division (float value1, float value2 )
    {
        return value1 / value2;
    }
}
